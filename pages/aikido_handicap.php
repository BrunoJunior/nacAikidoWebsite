<h1>L'Aïkido face au handicap</h1>
<div class="pull-left hidden-xs"><img class="img-responsive img-thumbnail img-marged" id="img" title="AIKIDO et HANDICAP" alt="AIKIDO et HANDICAP" src="img/DSCF1846.JPG" /></div>
<div>
	<p>
		A travers sa commission handicap, la FFAB travaille à l’ouverture de notre discipline vers les populations touchées par le handicap.
	</p>
	<p>
		Elle souhaite promouvoir cette démarche et de ce fait, encourage tous les clubs, présidents et enseignants à examiner cette piste de développement.
	</p>
	<p>
		La philosophie de l’aïkido, centrée sur la transformation et la progression personnelle est une discipline adaptée à toutes les personnes, y compris celles touchées par un handicap.
	</p>
	<p>
		L’absence de compétition et la distance raisonnable par rapport aux questions de mesure de performance (systèmes de grades et autres reconnaissances) en fait une méthode de développement personnel pertinente :
		<ul>
			<li>Un travail d’écoute de notre corps et de nos crispations, développement de nos sensations, un travail en relation avec l’autre, …</li>
			<li>Méthode globale qui parle à tous, quel que soit notre parcours, nos capacités, nos manques, …</li>
			<li>Développement personnel à notre rythme individuel…</li>
		</ul>
	</p>
	<p></p>
		A ce titre l’aïkido va donc contribuer à la lutte contre l’échec scolaire pour les plus jeunes, à la réduction des inégalités physiques, sociales et culturelles pour tous.
	</p>
	<p>
		C’est, de plus, le sens du nouveau périmètre de notre ministère de tutelle dans la dimension « cohésion sociale ».
	</p>
	<p>
		Au sein de notre fédération, quelques expériences ou pratiques existent déjà, parfois de longue date.
	</p>
	<p>
		Vous vous posez des questions ? Vous avez des craintes ? Elles sont légitimes !
	</p>
	<p>
		La commission handicap fédérale (CHF) est là pour répondre à vos questions, vous aider, vous accompagner et vous soutenir dans vos projets.
	</p>
	<p>
		Elle peut, dès maintenant, vous mettre en lien avec un club ou un professeur déjà dans une démarche similaire à votre préoccupation.
	</p>
	<p>
		Elle peut également clarifier, avec vous, les aspects responsabilités et cadre minimum pour l’accueil en club.
	</p>
	<p>
		La CFH travaille désormais à la mise en place d’une sensibilisation en même temps qu’une formation spécifique pour les professeurs.
	</p>
	<p>
		Que ceux et celles qui portent un réel intérêt et une réelle motivation pour ce projet, n’hésite pas à nous contacter !
	</p>
