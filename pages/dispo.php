<h1>Dispositions d’esprit pour la pratique de l’Aïkido</h1>
<div class="pull-left hidden-xs"><img class="img-responsive img-thumbnail img-marged" title="Dispositions d'esprit" alt="Emmanuel serrein" src="img/dispoesprit.jpg" /></div>
<b>Texte affiché à l'Aïkikaï de Tokyo dès 1932.</b>
<ol>
	<li>Un coup en Aïkido peut décider de la vie ou de la mort. Durant la pratique, obéissez aux instructions de celui qui dirige le cours. Ne transformez pas la pratique en un absurde test de force !</li>
	<li>L'Aïkido est une voie par laquelle au moyen du UN on peut atteindre les DIX MILLE ETRES. Même avec un seul adversaire, il ne faut pas uniquement se préoccuper de ce qui est devant, il est nécessaire de pratiquer en étant attentif aux quatre, aux huit directions.</li>
	<li>Il faut travailler dans la joie !</li>
	<li>Les enseignements de celui qui donne le cours ne représentent qu’un fragment de l‘Aïkido. Quand, par la recherche et l’entraînement quotidien et constant de soi-même, vous serez parvenu à la connaissance par le corps, le véritable usage des merveilles de l'Aïkido vous sera permis.</li>
	<li>L’entraînement journalier commence par TAI NO HENKA (préparation du corps), ensuite on pratique de plus en plus intensivement sans dépasser ses limites, ce qui permet à des personnes âgées de pratiquer sans risque de se blesser et d’atteindre le but de la pratique.</li>
	<li>L’Aïkido est une recherche qui tend par l’exercice du corps et de l’esprit à façonner un homme au coeur droit. Toutes les techniques sans exception sont secrètes et ne peuvent être montrées à ceux qui ne pratiquent pas ! Il faut éviter de les enseigner à ceux qui en feraient un mauvais usage.</li>
</ol>
<blockquote class="clear">
    <p>
    Aïki n’est pas l’art de la bataille avec l’ennemi ; ce n’est pas une technique de destruction de l’adversaire, c’est la voie de l’harmonisation du monde qui fait de l’humanité une seule nation.
    </p>
    <small>O.SENSEI</small>
</blockquote>
