<div class="page-header clear"><h3>L’Aïkido et les femmes</h3></div>
<div class="pull-right hidden-xs"><img class="img-responsive img-thumbnail img-marged" id="img" title="L'AÏKIDO et les FEMMES" alt="L'AÏKIDO et les FEMMES" src="img/123.jpg" /></div>
<blockquote>
	<p>
		Dans les activités sportives, la compétition oblige à une segmentation des publics par catégorie. Dans l’aïkido, il n’y a pas de compétition, femmes et hommes pratiquent ensemble, passent leur grades ensemble. L’aïkido ne propose pas de pratique féminine spécifique. Fallait-il l’inventer ? Sûrement pas !<br />
		Pratiquer l’aïkido ne suppose pas de posséder une force incroyable. Pour faire vite, il s’agit de<em> récupérer</em>  l’énergie de l’attaque de l’adversaire et de la lui <em>resservir</em>.<br />
		La plus grande part de l’apprentissage va résider dans cette expertise : être dans le temps de l’attaque, dans son contrôle sans toutefois contraindre. Bien évidemment, les femmes ont leur place dans cette pratique sans aucun aménagement nécessaire. Donc pas de place réservée pour les femmes mais pas de place refusée non plus.<br />
		Le principe d’aïkido, par essence même, se trouve à la croisée de ces qualités homme-femme, physiques et mentales. Il exige une nature martiale déterminée et disponible pour rester face au conflit sans toutefois se heurter contre ; une tonicité nécessaire pour prendre acte de cette attaque et une adaptabilité suffisante pour conduire l’adversaire au sol.<br />
		De plus, l’art martial en qualité de voie (do) s’appuie sur le postulat de la connaissance de soi. Il nous propose de trouver sa place. Trouver sa place accroît l’estime de soi et estompe le sentiment de péril identitaire. Cette finalité concerne tout pratiquant et éloigne toute tentative de partition homme-femme.<br />
		Commission Fédérale Féminines
	</p>
	<small>Marie-Christine VERNE</small>
