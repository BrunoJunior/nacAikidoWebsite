<h1>Qui peut pratiquer ?</h1>
<div class="page-header clear"><h3>L'Aïkido, une discipline accessible à tous</h3></div>
<p>
	Plutôt convaincre que vaincre, sans humilier et en respectant l'intégrité physique de l'un et de l'autre.<br />
	La recherche de l'Aïkido est celle de l'équilibre optimum de l'individu par rapport à lui-même, et par rapport à son environnement. Chacun peut se conformer à ses possibilités physiques, le but étant précisément de mieux les connaître. Une telle pratique est donc accessible à tous, hommes ou femme, de l'âge tendre à l'âge mûr.
</p>

<div class="page-header clear"><h3>L’Aïkido et les enfants</h3></div>
<div class="pull-left hidden-xs"><img class="img-responsive img-thumbnail img-marged" id="img" title="L'AÏKIDO et les enfants" alt="L'AÏKIDO et les enfants" src="img/enfants.jpg" /></div>
<p>
	L'aïkido est une discipline martiale de plus en plus pratiquée par <a href="http://www.ffabaikido.fr/fr/aikido_ffab.php?id_page=60">les enfants.</a><br />
	Des clubs d'enfants naissent dans chaque département et voient leur population augmenter chaque année.
</p>
<p>
	"Nous voulons tout simplement participer à <a href="http://www.ffabaikido.fr/fr/aikido_ffab.php?id_page=57">l'éducation des enfants</a> par un moyen : l'Aïkido.<br />
	C'est une discipline qui nous permet d'aborder certains problèmes éducatifs généraux.<br />
	Avec l'Aïkido nous désirons que l'enfant apprenne à agir, créer, montrer, aider, évoluer, organiser et ainsi s'ouvrir à sa propre sensibilité".
</p>
<p>
	Des stages régionaux, nationaux et internationaux leur sont proposés.<br />
	L'encadrement est composé de professeurs, d'éducateurs et de médecins.
</p>
<p>
	Une Commission Enfants Jeunes continue ses travaux au sein de l'école des cadres : son but est de former des enseignants  et de promouvoir toutes les activités intéressant les enfants et l'Aïkido.
</p>
