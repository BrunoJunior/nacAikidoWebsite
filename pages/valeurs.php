<h1>Les valeurs éducatives de l'Aïkido</h1>

<img class="img-responsive img-thumbnail img-marged" title="Valeurs éducatives" alt="Salut panoramique" src="img/valeduc.jpg" />

<span style="color: #FF0000;"><b>« Parfaite école d’enseignement, d’expérimentation et de développement des valeurs morales et physiques de la personne, l’Aïkido représente une méthode d’éducation complète »</b></span>

<ol>
	<li>
		<b>Physique</b> - La pratique de cette discipline améliore la santé par :
		<ul>
			<li>le développement harmonieux de toutes les parties du corps</li>
			<li>l’augmentation de la souplesse des articulations</li>
			<li>la correction de la colonne vertébrale</li>
			<li>le contrôle de la respiration</li>
			<li>la relaxation</li>
			<li>l’endurance</li>
		</ul>
	</li>
	<li>
		<b>Technique</b> - L’étude des mouvements impose :
		<ul>
			<li>l’étude du déséquilibre</li>
			<li>la connaissance et l’utilisation de l’énergie</li>
			<li>le développement des réflexes</li>
		</ul>
	</li>
	<li>
		<b>Morale</b> - La coordination physique et mentale, conditionne la réalisation de chaque technique, provoque en même temps que la transformation physique externe du corps une métamorphose interne morale.<br />
		Par le respect du bushido, code d’honneur et de morale traditionnelle des arts martiaux japonais et l’esprit particulier de non violence qui caractérise l’Aïkido, le pratiquant acquière les valeurs morales les plus élévées telles que :
		<ul>
			<li>politesse</li>
			<li>modestie</li>
			<li>bonté</li>
			<li>loyauté</li>
			<li>fidélité</li>
			<li>honneur</li>
			<li>courage et parfaite maîtrise de soi</li>
		</ul>
	</li>
</ol>

<b>Contrairement aux arts martiaux qui sont devenus des disciplines presque exclusivement sportives, la pratique de l’Aïkido exclut toute idée de compétition.</b>
